package com.example.demo.Models.Response;


public class StatisticHuman {
    private int numberOfDepartment;
    private int numberOfMachine;
    private int numberOfAccount;
    private int numberOfAdminAccount;
    private int numberOfEmployeeAccount;
    private int numberOfManagerAccount;

    public StatisticHuman() {
    }

    public int getNumberOfDepartment() {
        return numberOfDepartment;
    }

    public void setNumberOfDepartment(int numberOfDepartment) {
        this.numberOfDepartment = numberOfDepartment;
    }

    public int getNumberOfMachine() {
        return numberOfMachine;
    }

    public void setNumberOfMachine(int numberOfMachine) {
        this.numberOfMachine = numberOfMachine;
    }

    public int getNumberOfAccount() {
        return numberOfAccount;
    }

    public void setNumberOfAccount(int numberOfAccount) {
        this.numberOfAccount = numberOfAccount;
    }

    public int getNumberOfAdminAccount() {
        return numberOfAdminAccount;
    }

    public void setNumberOfAdminAccount(int numberOfAdminAccount) {
        this.numberOfAdminAccount = numberOfAdminAccount;
    }

    public int getNumberOfEmployeeAccount() {
        return numberOfEmployeeAccount;
    }

    public void setNumberOfEmployeeAccount(int numberOfEmployeeAccount) {
        this.numberOfEmployeeAccount = numberOfEmployeeAccount;
    }

    public int getNumberOfManagerAccount() {
        return numberOfManagerAccount;
    }

    public void setNumberOfManagerAccount(int numberOfManagerAccount) {
        this.numberOfManagerAccount = numberOfManagerAccount;
    }
}
